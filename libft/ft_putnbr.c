#include "libft.h"

void		ft_putnbr(int n)
{
	int		p;
	int		str[10];
	int		i;

	i = 0;
	p = 1;
	if (n < 0)
	{
		p = -1;
		ft_putchar('-');
	}
	if (n == 0)
		ft_putchar('0');
	while (n)
	{
		str[i] = (n % 10) * p;
		n = n / 10;
		i++;
	}
	while (i)
	{
		i--;
		ft_putchar(str[i] + '0');
	}
}
